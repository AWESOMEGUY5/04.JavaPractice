public class OperatorTest {
    public static void main(String[] args) {
        int x = 10, y = 20;

        System.out.println("x > y : "+ (x > y));
        System.out.println("x > y : "+ (x < y));
        System.out.println("x == y : "+ (x == y));
        System.out.println("x == 10 : "+ (x == 10));
        System.out.println("x == 20 : "+ (x == 20));

        System.out.println("x != y : "+ (x != y));
        System.out.println("x != 20 : "+ (x != 20));

        System.out.println("x >= y : "+ (x >= y));
        System.out.println("x <= y : "+ (x <= y));
    }
}
