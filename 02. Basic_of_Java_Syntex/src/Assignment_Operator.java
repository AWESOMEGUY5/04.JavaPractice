public class Assignment_Operator {
    public static void main(String[] args) {
        int a = 50, b = 10;

        System.out.printf("%d\n", a += b);
        System.out.printf("%d\n", a -= b);
        System.out.printf("%d\n", a *= b);
        System.out.printf("%d\n", a /= b);
        System.out.printf("%d\n", a %= b);
        System.out.printf("%d\n", a = b);
    }
}
